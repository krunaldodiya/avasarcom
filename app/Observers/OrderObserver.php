     * Handle the Order "created" event.
     *
     * @param  \App\Models\Order  $order
     * @return void
     */
    public function created(Order $order)
    {
        $product = Product::find($order->product_id);

        $product->quantity = $product->quantity - $order->quantity;

        $product->save();
    }

